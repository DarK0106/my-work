[PPT 구성안] 교육기관 관리 시스템 프로젝트
1. 프로젝트 개요 (1-2 슬라이드)
• 프로젝트 명칭: 교육센터 통합 관리 시스템 (Education Center Management System)
• 개발 목표: 교육생, 강사, 과정, 근태, 자원(PC/사물함) 등 교육기관 운영에 필요한 전반적인 데이터를 효율적으로 관리하고, 
자동화된 비즈니스 로직(수당 지급, 중도 탈락 처리 등)을 구현함.
• 개발 기간 및 팀 구성:
    ◦ 기간: 2026.02.03 (요구분석) ~ 2026.02.10 (DB 구현 완료)
    ◦ 팀원 (2조): 박지명, 김민도, 조영선, 홍태훈, 황윤재
• 개발 환경:
    ◦ DBMS: Oracle Database
    ◦ Language: SQL (ANSI-SQL), PL/SQL 및 오브젝트 (Function, Procedure, Trigger, View)
• 협업 도구:
    ◦ Figma
    ◦ Miro
    ◦ Google Sheet
2. 시스템 소개 (2-3 슬라이드)
• 교육기관 관리 시스템의 필요성:
    ◦ 연간 다수의 과정(평균 12개)과 과목(30~50개) 운영에 따른 데이터 복잡성 해결.
    ◦ 출결 기반의 훈련수당 계산 및 수료 후 취업 지원금 관리의 자동화 필요.
• 주요 관리 대상:
    ◦ 인적 자원: 교육생(Student), 강사(Instructor/Affiliated Instructor), 관리자(Admin), 유저(Users)
    ◦ 교육 과정: 개설 과정(Registered Course), 개설 과목(Opened Subject), 커리큘럼(Curriculum), 과목(Subject), 과정(Course)
    ◦ 물적 자원: 강의실(Lecture), 컴퓨터(Computer), 사물함(Locker), 교재(Book)
    ◦ 기타: 성적, 지원금, 개인 배정, 평가 등..
• 시스템 핵심 기능 개요:
    ◦ 관리자: 기초 정보 관리, 교사/교육생 계정 관리, 성적/출결/지원금 관리.
    ◦ 교사: 강의 스케줄 조회, 배점 및 성적 입력, 출결 관리.
    ◦ 교육생: 성적 조회, 개인 출결 현황 조회.
3. 데이터베이스 설계 (3-4 슬라이드)
• 테이블 계층 구조 (Depth 1~5): 데이터 의존성에 따라 5단계로 설계.
    ◦ Depth 1: 근태종류, 강의실, 컴퓨터 상태, 권한, 과정, 과목
    ◦ Depth 2: 사물함, 컴퓨터, 유저, 등록과정, 커리큘럼, 강의가능과목, 학원교사, 관리자
    ◦ Depth 3: 질문, 수강생, 개설과목, 등록교사, 자주묻는질문
    ◦ Depth 4: 근태, 사물함 개인 배정, 컴퓨터 개인 배정, 훈련지원금, 취업상태, 취업성공수당, 강의평가, 교사평가, 시험, 교재, 배점, 답변
    ◦ Depth 5: 시험성적
• 주요 엔티티 관계:
    ◦ Course vs Student: 1:N 관계 (하나의 등록된 과정에 여러 학생 소속).
    ◦ User vs Student/Admin: 슈퍼타입/서브타입 유사 구조 (USERS 테이블의 정보를 STUDENT가 참조).
    ◦ Lecture vs Course: 특정 강의실이 특정 기간 동안 하나의 과정에 할당됨.
4. 요구사항 분석 (2-3 슬라이드)
• A그룹 - 관리자 요구사항 (A-1 ~ A-10):
    ◦ 교육 과정 관리: 개설 과정 및 과목 등록, 강의실 배정, 교재 및 담당 교사 지정.
    ◦ 교육생 및 성적 관리: 교육생 등록, 중도 탈락 처리, 시험 및 성적 조회.
    ◦ 재정 및 취업 관리: 훈련지원금 지급 내역 조회, 취업 성공 수당(6개월 후) 관리.
• B그룹 - 교사 요구사항 (B-1 ~ B-5):
    ◦ 강의 관리: 본인의 강의 스케줄 및 배정된 수강생 조회.
    ◦ 성적 처리: 과목별 배점(출결/필기/실기) 설정 및 성적 입력.
• C그룹 - 교육생 요구사항 (C-1 ~ C-3):
    ◦ 정보 조회: 본인의 성적 확인, 일별/월별 근태 현황 조회.
5. 주요 구현 기능 (PL/SQL) (4-6 슬라이드)
5-1. Function 구현
• 성별 자동 추출 (FN_GET_GENDER): 주민번호 뒷자리를 기반으로 '남자', '여자' 반환.
• 우수 강사 판별 (FN_CHECK_EXCELLENT_TEACHER): 강의 평가 평균 점수가 9점 이상인 경우 '우수 교사' 반환.
• 결석일 계산 (func_calculate_absence): 지각/조퇴/외출 3회를 결석 1회로 환산하여 총 결석일수 반환.
• 훈련지원금 대상자 (func_apply_allowance): 현재 수강 중이며 지원금을 신청하지 않은 대상자 커서 반환.
• 취업 지원금 미지급자 (FN_FIND_UNPAID_GRADUATES): 취업 후 6개월 경과했으나 수당 미지급 상태인 수료생 식별 ('Y'/'N').
5-2. View 구현
• 사용자-과정 정보 뷰 (vw_user_course_info): 학생 정보, 수강 과정, 담당 교사 정보를 통합 조인하여 관리자가 한눈에 파악.
• 재고 컴퓨터 현황 (vw_storage_computer): 창고에 보관 중(미사용)인 컴퓨터 목록 조회.
• 학생 성적 뷰 (vw_student_score): 학생별, 과목별, 시험 유형별 점수 통합 조회.
• 출결 현황 뷰 (VW_STUDENT_ATTENDANCE_STATUS): 학생별 출석률(%)을 계산하여 실시간 조회 (결석 제외 카운트).
5-3. Procedure 구현
• 중도 탈락 처리 (proc_handle_course_drop): 출석률이 80% 미만인 학생의 상태를 '중도포기'로 자동 업데이트.
• 훈련수당 지급 (PROC_GIVE_TRAINING_ALLOWANCE): 월별 결석 횟수를 차감(1회당 1만원)하여 지급액 계산 후 테이블에 삽입.
• 컴퓨터 상태 일괄 변경 (proComputerStatus): 특정 범위의 컴퓨터 상태(사용중/미사용)를 일괄 업데이트.
5-4. Trigger 구현
• 정원 초과 방지 (trg_check_student_limit): 수강생 등록 시 강의실 정원(Capacity)과 현재 인원을 비교하여 초과 시 에러 발생 (RAISE_APPLICATION_ERROR).
• 권한 자동 업데이트 (trg_student_insert_update_user_state): 학생 상태가 '수강중'이 되면 USERS 테이블의 권한을 4번(학생)으로, '수강종료' 시 3번(일반유저)으로 자동 변경.
• 점수/배점 유효성 검증 (TRG_CHECK_VALID_SCORE, TRG_CHECK_POINT_SUM): 시험 점수 0~100점 범위 검사, 배점 합계 100점 검사.
• 자원 상태 자동 관리 (trgLockerDateStatus, trgComputerDateStatus): 배정 기간 내에만 '사용중'으로 상태 변경, 기간 만료 시 '미사용' 처리.
6. 쿼리 구현 사례 (2-3 슬라이드)
• ANSI-SQL 기반 복합 조회:
    ◦ JOIN 활용: 학생(STUDENT), 유저(USERS), 과정(COURSE), 고용(EMPLOYMENT_STATUS) 테이블을 조인하여 취업 성공자 상세 조회.
    ◦ LEFT OUTER JOIN 활용: 취업에 성공했으나 수당을 받지 못한 인원을 찾기 위해 EMPLOYMENT_SUCCESS_ALLOWANCE 테이블과 조인.
• 집계 함수 및 서브쿼리 활용:
    ◦ 성적 우수자 조회: MAX() 서브쿼리를 사용하여 특정 과목의 최고 점수 획득 학생 조회.
    ◦ 통계 조회: GROUP BY와 COUNT(CASE WHEN ...)을 사용하여 과정별 취업 성공 인원 및 전체 인원 대비 통계 산출.
    ◦ 출결 통계: 조퇴 횟수가 많은 순으로 정렬하여 상위 1명 조회 (FETCH FIRST 1 ROWS ONLY).
7. 핵심 비즈니스 로직 (2-3 슬라이드)
• 출결 및 중도 탈락 로직:
    ◦ 로직: 지각 + 조퇴 + 외출 합계 3회 당 결석 1회로 환산.
    ◦ 자동화: 총 수업 일수 대비 출석률 계산 후 80% 미만 도달 시 프로시저를 통해 '중도포기' 처리.
• 재정 지원 로직:
    ◦ 훈련수당: 기본급(30만원)에서 결석 1회당 1만원씩 차감 계산. 중복 지급 방지 로직 포함.
    ◦ 취업 성공 수당: 수료 후 취업 성공 상태 유지 + 6개월 경과 시 지급 대상자(Y)로 판별.
• 자원 할당 로직:
    ◦ 사물함 및 컴퓨터는 학생의 수강 기간(배정일~종료일) 동안에만 유효하며, 기간 경과 시 트리거에 의해 자동으로 '미사용' 처리되어 자원 회수.
8. 테스트 및 검증 (1-2 슬라이드)
• 유효성 검증 테스트:
    ◦ 배점 합계 110점 입력 시도 -> ORA-20002 에러 발생 확인 (트리거 동작 성공).
    ◦ 시험 점수 150점 또는 -10점 입력 시도 -> ORA-20001 에러 발생 확인.
    ◦ 강의실 정원 초과 등록 시도 -> RAISE_APPLICATION_ERROR 발생 확인.
• 프로시저 동작 검증:
    ◦ proc_handle_course_drop 실행 전후 수강생 상태 변화('수강중' -> '중도포기') 및 출석률 로그 출력 확인.
    ◦ 훈련수당 지급 프로시저 실행 후 TRAINING_ALLOWANCE 테이블에 해당 월 데이터 생성 확인.
9. 개선사항 및 향후 계획 (1 슬라이드)
• 프로젝트 난관 및 해결:
    ◦ 강의실 정원 체크 시 동시성 문제와 테이블 간 관계(Lecture-Student)의 복잡성 -> 뷰(VIEW_COURSE_CAPACITY)를 활용한 트리거 구현으로 해결.
• 개선 가능 부분:
    ◦ 현재 콘솔 출력(DBMS_OUTPUT) 위주의 결과 확인을 Java/Web 연동을 통해 UI로 시각화 필요.
    ◦ 시험 문제 은행 및 온라인 시험 응시 기능 추가 구현.
• 향후 계획:
    ◦ 강사 평가 데이터를 활용한 인센티브 자동 계산 시스템 확장.
