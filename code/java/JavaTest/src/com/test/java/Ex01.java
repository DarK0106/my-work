package com.test.java;

// Ex01 이라는 이름을 가진 클래스(Class)
//- 클래스는 코드의 집합
public class Ex01 // public class Ex01 이 부분을 헤더라고 부름
//괄호와 괄호 사이의 공간을 몸통, body 라고 부름
{ 

	// main 이라는 이름의 메서드(Method)
	// - 메서드도 코드의 집합
	public static void main(String[] args)
	{

		// println 메서드
		System.out.println("첫번째 예제");

	}
	
	public static void test()
	{
		System.out.println("테스트 메서드");
	}

}
